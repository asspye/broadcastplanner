var cp1251Reverse = buildCP1251Reverse()

func buildCP1251Reverse() map[rune]byte {
	m := make(map[rune]byte, 128)
	for b, r := range cp1251High {
		m[r] = b
	}
	// 0xC0..0xFF map linearly to Cyrillic А..я (U+0410..U+044F).
	for i := 0; i < 64; i++ {
		m[rune(0x0410+i)] = byte(0xC0 + i)
	}
	return m
}
